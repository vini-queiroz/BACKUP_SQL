-- produção de cames por ordem de produção --------------------

SELECT D3_OP,
	D3_COD, 
	A.B1_DESC,
	D3_EMISSAO,
	SUM(D3_QUANT) AS TOTAL_PROD

	FROM(SELECT D3_TM, 
		D3_COD, 
		B1_DESC, 
		D3_UM, 
		D3_QUANT, 
		H6_OPERADO, 
		Z5_NOME, 
		D3_CF,
		D3_OP, 
		D3_DOC, 
		D3_EMISSAO, 
		B1_GRUPO, 
		D3_CC, 
		D3_TIPO, 
		D3_USUARIO

		FROM SD3010 	INNER JOIN SB1010 ON B1_COD = D3_COD 
			LEFT JOIN SH6010 ON H6_IDENT = D3_IDENT
			INNER JOIN SZ5010 ON Z5_COD = H6_OPERADO

		WHERE D3_EMISSAO >= '20240701' and --BETWEEN '20240101' AND '20240425' AND
			D3_CF = 'PR0' AND
			B1_GRUPO LIKE '032') A

	GROUP BY D3_EMISSAO,
		A.D3_COD,
		D3_OP, 
		A.B1_DESC

	ORDER BY D3_EMISSAO