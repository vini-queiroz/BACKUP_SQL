SELECT
	CASE	
		WHEN C7_EMISSAO BETWEEN '20240603' AND '20240609' THEN 'SEM 23'
		WHEN C7_EMISSAO BETWEEN '20240610' AND '20240616' THEN 'SEM 24'
		WHEN C7_EMISSAO BETWEEN '20240617' AND '20240623' THEN 'SEM 25'
		WHEN C7_EMISSAO BETWEEN '20240624' AND '20240630' THEN 'SEM 26'
		ELSE '' END AS SEMANA 
	, C7_NUM + C7_ITEM AS PEDIDO_COMP
	, C7_PRODUTO
	, C7_DESCRI
	, C7_QUANT
	, C7_EMISSAO
	, D1_EMISSAO AS ENTREGA
	, A2_NOME
	FROM 
		SC7010
		INNER JOIN SG1010 ON C7_PRODUTO = G1_COMP AND SG1010.D_E_L_E_T_<>'*'
		INNER JOIN SA2010 ON C7_FORNECE = A2_COD
		INNER JOIN SD1010 ON C7_NUM + C7_ITEM = D1_PEDIDO + D1_ITEMPC AND SD1010.D_E_L_E_T_<>'*'
	WHERE 
		C7_FORNECE = '000984'
		AND SC7010.D_E_L_E_T_<>'*'
		AND C7_EMISSAO BETWEEN '20240603' AND '20240630'
	ORDER BY 
		C7_EMISSAO

