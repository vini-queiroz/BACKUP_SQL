select 
	H6_OP
	, H6_PRODUTO
	, B1_DESC
	, H6_OPERAC
	, G2_DESCRI
	, H6_QTDPROD 
	, H6_DTAPONT
	, H6_TEMPO
from SH6010
	INNER JOIN SG2010 ON H6_PRODUTO + H6_OPERAC = G2_PRODUTO + G2_OPERAC and SG2010.D_E_L_E_T_<>'*'
	INNER JOIN SB1010 ON H6_PRODUTO = B1_COD AND SB1010.D_E_L_E_T_<>'*'
where H6_OPERADO like '54274'
	and SH6010.D_E_L_E_T_<>'*'
	and H6_OP like 'A00ZUT%'